enum DolchLevel {
  prePrimer(
    id: 'pre_primer',
    displayName: 'Pre-Primer',
  ),
  primer(
    id: 'primer',
    displayName: 'Primer',
  ),
  firstGrade(
    id: 'first_grade',
    displayName: 'First Grade',
  ),
  secondGrade(
    id: 'second_grade',
    displayName: 'Second Grade',
  ),
  thirdGrade(
    id: 'third_grade',
    displayName: 'Third Grade',
  );

  const DolchLevel({
    required this.id,
    required this.displayName,
  });

  /// Stable identifier intended for saved records.
  final String id;

  /// Human-readable text intended for the interface.
  final String displayName;
}

